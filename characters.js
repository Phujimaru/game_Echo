// เวอร์ชัน "สาธารณะ" ส่งให้ client (ตัด effect ภายในออก เหลือชื่อ/คำอธิบาย/ค่าใช้)
function publicRoster() {
  const pub = (s) => (s ? { name: s.name, desc: s.desc, cost: s.cost, img: s.img, ammo: s.ammo } : null);
  return CHARACTERS.map((c) => ({
    id: c.id,
    name: c.name,
    avatar: c.avatar,
    img: c.img,
    locked: !!c.locked,
    difficulty: c.difficulty || "easy", // ความยากในการเล่น (ใช้แบ่งหน้าเลือกตัวละคร)
    passive: c.passive ? { name: c.passive.name, desc: c.passive.desc } : null,
    basic: pub(c.basic),
    secondary: pub(c.secondary),
    ultimate: pub(c.ultimate),
    // สกิลกลางคืน (โอเบรอน / โคโตเนะ) — ตัวอื่นเป็น null
    basicNight: pub(c.basicNight),
    secondaryNight: pub(c.secondaryNight),
    ultimateNight: pub(c.ultimateNight),
    // ชเรด เอลัน: สกิลพื้นฐาน/สกิลรอง/ท่าไม้ตายหลังรวมร่าง (อควาเรียน สปาด้า) — ตัวอื่นเป็น null
    basic2: pub(c.basic2),
    secondary2: pub(c.secondary2),
    ultimate2: pub(c.ultimate2),
    // อควาเรียน: สกิลรอง "คืนร่าง" + ท่าไม้ตาย 4 แบบ (โซล่า/มาร์/ลูน่า/ปีกแห่งสุริยัน) — ตัวอื่นเป็น null
    secondaryRevert: pub(c.secondaryRevert),
    ultimateSolar: pub(c.ultimateSolar),
    ultimateMars: pub(c.ultimateMars),
    ultimateLuna: pub(c.ultimateLuna),
    ultimateGodwing: pub(c.ultimateGodwing),
  }));
}

module.exports = { CHARACTERS, CHAR_BY_ID, POSITION_COLORS, publicRoster };
